let _language = readLanguage();

function readLanguage() {
  try {
    const value = localStorage.getItem(KEY);
    return value === 'en' ? 'en' : DEFAULT_LANGUAGE;
  } catch {
    return DEFAULT_LANGUAGE;
  }
}

export function getLanguage() {
  return _language;
}

export function getLocale() {
  return _language === 'en' ? 'en' : 'no';
}

export function setLanguage(language) {
  _language = language === 'en' ? 'en' : 'no';
  try { localStorage.setItem(KEY, _language); } catch {}
  document.documentElement.lang = _language;
  window.dispatchEvent(new CustomEvent('tabtabtab:languagechange', {
    detail: { language: _language },
  }));
}

export function toggleLanguage() {
  setLanguage(_language === 'no' ? 'en' : 'no');
}

export function t(key, vars = {}) {
  const template = STRINGS[_language][key] ?? STRINGS.no[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ''));
}

export function tabCountLabel(count) {
  return t(count === 1 ? 'tab_singular' : 'tab_plural');
}

export function songbookDisplayName(songbook) {
  if (!songbook) return '';
  if (songbook.id === 'fav') return t('favorites');
  if (songbook.id === 'ug-import-main') return t('my_ug_imports');
  return songbook.name;
}
